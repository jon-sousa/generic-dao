<?php

namespace jon\dao\models;

class Usuario 
{
    public function __construct(
        private string $nome,
        private string $cpf
    ){}
 
    public function getNome()
    {
        return $this->nome;
    }

    public function setNome($nome)
    {
        $this->nome = $nome;

        return $this;
    }

    public function getCpf()
    {
        return $this->cpf;
    }

    public function setCpf($cpf)
    {
        $this->cpf = $cpf;

        return $this;
    }
}